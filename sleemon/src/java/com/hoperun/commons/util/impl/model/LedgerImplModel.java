public class LedgerImplModel {
	private String LEDGER_NO;
	private String LEDGER_NAME;
	private String LEDGER_NO_P;
	private Integer STATE;
	

	public Integer getSTATE() {
		return STATE;
	}
	public void setSTATE(Integer sTATE) {
		STATE = sTATE;
	}
	public String getLEDGER_NO() {
		return LEDGER_NO;
	}
	public void setLEDGER_NO(String lEDGERNO) {
		LEDGER_NO = lEDGERNO;
	}
	public String getLEDGER_NAME() {
		return LEDGER_NAME;
	}
	public void setLEDGER_NAME(String lEDGERNAME) {
		LEDGER_NAME = lEDGERNAME;
	}
	public String getLEDGER_NO_P() {
		return LEDGER_NO_P;
	}
	public void setLEDGER_NO_P(String lEDGERNOP) {
		LEDGER_NO_P = lEDGERNOP;
	}

}
